export const getRandomEmoji = () => {
    const codePoint = EMOJI_BLOCKS[Math.floor(Math.random() * EMOJI_BLOCKS.length)] 
        + Math.floor(Math.random() * 16)
    return String.fromCodePoint(codePoint)
}

